package com.example.schoolmanagement_01.core.sco;

public class ReportSCO {

    private String week;

    private String classRoom;

    private String collectionRuleCode;

    public ReportSCO() {
    }

    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }

    public String getClassRoom() {
        return classRoom;
    }

    public void setClassRoom(String classRoom) {
        this.classRoom = classRoom;
    }

    public String getCollectionRuleCode() {
        return collectionRuleCode;
    }

    public void setCollectionRuleCode(String collectionRuleCode) {
        this.collectionRuleCode = collectionRuleCode;
    }
}
